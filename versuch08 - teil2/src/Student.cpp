// Konstruktor
Student::Student(int matrikelnummer, string name2, string vorname2, string datum):
	matNr(matrikelnummer), name(name2), vorname(vorname2), geburtsdatum(datum)
{
}

//////////////////////////////////////////////////////////////////////////////
// Gleichheitsoperator
bool Student::operator == (const Student& stud) const
{
	return (matNr == stud.matNr);
}

//////////////////////////////////////////////////////////////////////////////
// Ungleichheitssoperator
bool Student::operator < (const Student& stud) const
{
	return (matNr < stud.matNr);
}

//////////////////////////////////////////////////////////////////////////////
// Ausgabe fÃ¼r Studenten
ostream& operator << (ostream& out, Student& stud)
{
	out << stud.matNr << ": " << stud.vorname << " " << stud.name << " (" << stud.geburtsdatum << ")" << endl;

	return out;
}

//////////////////////////////////////////////////////////////////////////////
// Getter for matNr
int Student::getMatNr() const
{
	return matNr;
}
